function RenderSkinCollection(DataText){
    const lines = DataText.split('\n').map(line => line.trim()).filter(line => line.length > 0);
    let obtainedOutput = '';
    let obtainableOutput = '';
    lines.forEach(line => {
        const [name, icon, isObtainedStr] = line.split(',').map(item => item.trim());
        const IsObtained = isObtainedStr === 'true';
        const item =
            `<section>
                <img src="assets/renders/skins/${CharacterName}/${icon}" alt="${name}">
                <div>
                    <h3>${name}</h3>
                </div>
            </section>\n`;
        if (IsObtained) {
            obtainedOutput += item;
        } else {
            obtainableOutput += item;
        }
    });
    const obtainedContainer = document.getElementById('obtained');
    const obtainableContainer = document.getElementById('obtainable');
    obtainedContainer.innerHTML = obtainedOutput;
    obtainableContainer.innerHTML = obtainableOutput;
}